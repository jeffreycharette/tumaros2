							<p>In a medium saucepan, combine water, tomato paste, oil, chili powder, jalapeños, garlic, cumin, and onions. Stir until smooth and heated through. Spoon about 1/3 of the sauce in bottom of a medium baking dish. To assemble each enchilada, wet both sides of tortilla in remaining sauce, place 1⁄4 of the salmon down the center of the tortilla and sprinkle with 1 Tablespoon of cheese. Roll tortilla and place seam side down in baking dish. Continue with remaining 3 tortillas. Pour remaining sauce over the four enchiladas. Bake for 10 minutes in a 350°F oven until hot and bubbly. Top with sour cream and serve.</p>
